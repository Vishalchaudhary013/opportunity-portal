const SocialAuthButtons = ({ mode = 'signup' }) => {
  const handleSocialAuth = (provider) => {
    // Redirect to backend OAuth route
    const redirectURL = socialAuthURL[provider];

    if (redirectURL) {
      window.location.href = redirectURL;
    }
  };

  return (
    <div className="space-y-3">
      <p className="text-center text-xs text-gray-500 uppercase tracking-widest mb-4">
        Continue with
      </p>

      {/* 2x2 grid layout */}
      <div className="grid grid-cols-2 gap-2.5">
        {socialProviders.map(({ id, label, Icon }) => (
          <button
            key={id}
            type="button"
            onClick={() => handleSocialAuth(id)}
            className="btn-social group"
            aria-label={`${mode === 'signup' ? 'Sign up' : 'Sign in'} with ${label}`}
          >
            <Icon />
            <span className="text-gray-300 group-hover:text-white transition-colors text-sm font-medium">
              {label}
            </span>
          </button>
        ))}
      </div>
    </div>
  );
};

